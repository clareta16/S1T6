package n1exercici1;

import java.util.ArrayList;

public class NoGenericMethods  {
    private int dos;
    private int tres;
    private int quatre;

    public NoGenericMethods(int dos, int tres, int quatre) {
        this.dos = dos;
        this.tres = tres;
        this.quatre = quatre;
    }

    public int getDos() {
        return dos;
    }

    public int getTres() {
        return tres;
    }

    public int getQuatre() {
        return quatre;
    }

    @Override
    public String toString() {
        return super.toString();
    }


}

